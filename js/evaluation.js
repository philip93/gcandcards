var UNKNOWN_TYPE = "unknown";
var ACE_TYPE = "ace";
var GOOD_TYPE = "good";
var DECENT_TYPE = "decent";
var BAD_TYPE = "bad";
var Evaluation = function(eid) {
    var hash = {};
    for (var i = 0; i < evaluation_data[eid].data.length; i++) {
        var d = evaluation_data[eid].data[i];
        hash[d[0]] = {
            worth: d[1],
            rbworth: d[2],
            usability: d[3],
            rbusability: d[4],
            goods: d[5],
            decents: d[6],
            bads: d[7],
            getWorth: function () { return this.worth; },
            getRebirthWorth: function () { return this.rbworth; },
            getUsability: function () { return this.usability; },
            getRebirthUsability: function () { return this.rbusability; },
            getGoodTypes: function () { return this.goods; },
            getDecentTypes: function () { return this.decents; },
            getBadTypes: function () { return this.bads; },
            getRank: function(t) {
                var ts = this.isGood(t) ? this.getGoodTypes() : (
                    this.isDecent(t) ? this.getDecentTypes() : (
                        this.isBad(t) ? this.getBadTypes() : new Array()
                    )
                );
                for (var i = 0; i < ts.length; i++) {
                    if (ts[i][0] == t)
                        return ts[i][1];
                }
                var n = t.getNormalType();
                for (var i = 0; i < ts.length; i++) {
                    if (ts[i][0] == n)
                        return ts[i][1];
                }
                return UNKNOWN_RANK;
            },
            has: function(t, es) {
                for (var i = 0; i < es.length; i++) {
                    if (es[i][0] == t)
                        return true;
                }
                return false;
            },
            isGood: function(t) {
                var goods = this.getGoodTypes();
                if (this.has(t, goods))
                    return true;

                if (t.isRebirthType()) {
                    var n = t.getNormalType();
                    return this.has(n, goods) && !this.has(t, this.getDecentTypes()) && !this.has(t, this.getBadTypes());
                }

                return false;
            },
            isDecent: function(t) {
                var decents = this.getDecentTypes();
                if (this.has(t, decents))
                    return true;

                if (t.isRebirthType()) {
                    var n = t.getNormalType();
                    return this.has(n, decents) && !this.has(t, this.getGoodTypes()) && !this.has(t, this.getBadTypes());
                }

                return false;
            },
            isBad: function(t) {
                var bads = this.getBadTypes();
                if (this.has(t, bads))
                    return true;

                if (t.isRebirthType()) {
                    var n = t.getNormalType();
                    return this.has(n, bads) && !this.has(t, this.getGoodTypes()) && !this.has(t, this.getDecentTypes());
                }

                return false;
            }
        };
    }
    return {
        getName: function() {
            return evaluation_data[eid].name;
        },
        getURL: function() {
            return evaluation_data[eid].url;
        },
        getDescription: function() {
            return evaluation_data[eid].description;
        },
        getType: function(g, t) {
            if (t == Type.ace || t == Type.acer)
                return ACE_TYPE;

            var f = hash[g.id];
            if (f) {
                if (f.isGood(t))
                    return GOOD_TYPE;
                else if (f.isDecent(t))
                    return DECENT_TYPE;
                else if (f.isBad(t))
                    return BAD_TYPE;
            }
            return UNKNOWN_TYPE;
        },
        getRank: function(g, t) {
            var f = hash[g.id];
            if (f)
                return f.getRank(t);
            return UNKNOWN_RANK;
        },
        getWorth: function(g) {
            var f = hash[g.id];
            if (f)
                return f.getWorth();
            return -1;
        },
        getRebirthWorth: function(g) {
            var f = hash[g.id];
            if (f)
                return f.getRebirthWorth();
            return -1;
        },
        getUsability: function(g) {
            var f = hash[g.id];
            if (f)
                return f.getUsability();
            return -1;
        },
        getRebirthUsability: function(g) {
            var f = hash[g.id];
            if (f)
                return f.getRebirthUsability();
            return -1;
        }
    };
};
